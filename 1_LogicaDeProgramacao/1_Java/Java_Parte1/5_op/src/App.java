public class App {
    public static void main(String[] args) {
        int x = 5, y = 9, z = 10;
        x++; // x = x + 1 // x = 5 + 1 -> x = 6
        System.out.println(x); // 6
        System.out.println(y); // 9
        y--; // y = y - 1 //8
        System.out.println(y); // 8
        z += 5;
        System.out.println(z);
        z -= 5;
        System.out.println(z);
    }
}
