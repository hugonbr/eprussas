# Escreva um algoritmo que leia 2 números inteiros e mostre o resultado de suas 4 operações básicas: +,-,*,/

n1 = int(input("Digite o n1: "))
n2 = int(input("Digite o n2: "))
# 4 + 2 = 6
print(f'{n1} + {n2} = {n1+n2}')
print(f'{n1} - {n2} = {n1-n2}')
print(f'{n1} * {n2} = {n1*n2}')
print(f'{n1} / {n2} = {n1/n2}')